const translations: Record<Language, typeof en> = { en, ar, ur };

/**
 * Get a translated string by dot-path key. Supports {0}, {1}, ... placeholders.
 *
 * Usage:
 *   t('nav.dashboard')           → "Dashboard" or "لوحة التحكم"
 *   t('dashboard.transactions', '25')  → "25 transactions" or "25 عملية"
 */
export function createTranslator(lang: Language) {
  const dict = translations[lang] || translations.en;

  return function t(key: string, ...args: (string | number)[]): string {
    let value = getNestedValue(dict, key);
    // Fallback to english if key not found
    if (value === key) {
      value = getNestedValue(translations.en, key);
    }
    // Replace placeholders {0}, {1}, ...
    args.forEach((arg, i) => {
      value = value.replace(`{${i}}`, String(arg));
    });
    return value;
  };
}

/**
 * Get text direction for a language.
 */
export function getDirection(lang: Language): 'ltr' | 'rtl' {
  return lang === 'ar' || lang === 'ur' ? 'rtl' : 'ltr';
}
